public class AscendingDescending {
	/**
	 * 
	 * @param args
	 */

	public static void main(String[] args) {
		/**
		 * to find the ascending/descending 
		 */
	
		int j = 5;
		System.out.println("---Results---");
		for(int i = 1; i <= 5; i++) {
			System.out.println(i + "\t" + j);
			j--;
		}
		

	}

}
