public class EvenNo {
	/**
	 * 
	 * @param args
	 */


	public static void main(String[] args) {
		/**
		 * to print even number from 1 to 20
		 */
		for(int i = 1; i <= 20; i++) {
			if(i % 2 == 0){
				System.out.println(i);
			}
			
		}

	}

}
